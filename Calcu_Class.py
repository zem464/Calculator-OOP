# Create class method
class calculator:

    # Create instance method for addition
    def add(self, inp_num1, inp_num2):
        sum = inp_num1 + inp_num2
        return sum

    # Create instance method for subtraction
    def subtract(self, inp_num1, inp_num2):
        difference  = inp_num1 - inp_num2
        return difference

    # Create instance method for multiplication
    def multiply(self, inp_num1, inp_num2):
        product = inp_num1 * inp_num2
        return product

    # Create instance method for division
    def divide(self, inp_num1, inp_num2):
        quotient = inp_num1 / inp_num2
        return quotient